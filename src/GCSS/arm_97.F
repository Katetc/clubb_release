     .           7.84870,         7.61950,
     .          24.22600,        64.27100,
     .          88.21300,        82.50400
     .  /),
     .  ! Evaporation         [W/m^2]
     .  LE = (/
     .         143.45900,        63.82500,
     .          15.08800,        13.57900,
     .          63.74400,       170.96400,
     .         271.23599,       269.89499,
